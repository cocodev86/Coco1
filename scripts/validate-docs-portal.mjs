import { existsSync, readFileSync, readdirSync, statSync } from "node:fs";
import path from "node:path";

const root = process.cwd();
const docsRoot = path.join(root, "docs");
const failures = [];
const requiredPortalFiles = [
  "app/docs/layout.tsx",
  "app/docs/[[...slug]]/page.tsx",
  "app/docs/DocsShell.tsx",
  "app/docs/DocsSidebar.tsx",
  "app/docs/DocsSearch.tsx",
  "app/docs/MarkdownDocument.tsx",
  "app/docs/docs.module.css",
  "app/docs/loading.tsx",
  "app/docs/not-found.tsx",
  "lib/docs.ts",
  "docs/CONTRIBUTING.md",
];

for (const file of requiredPortalFiles) {
  if (!existsSync(path.join(root, file))) failures.push(`${file} is required.`);
}

const categoryDirectories = ["mos", "playbook", "client-operations", "platform", "strategy", "templates"];
for (const category of categoryDirectories) {
  if (!existsSync(path.join(docsRoot, category))) failures.push(`docs/${category} is required.`);
}

function walk(directory) {
  if (!existsSync(directory)) return [];
  return readdirSync(directory, { withFileTypes: true }).flatMap((entry) => {
    const fullPath = path.join(directory, entry.name);
    return entry.isDirectory() ? walk(fullPath) : entry.name.toLowerCase().endsWith(".md") ? [fullPath] : [];
  });
}

function slugify(value) {
  return value.toLowerCase().replace(/&/g, " and ").replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
}

function routeForSource(fullPath) {
  const relative = path.relative(docsRoot, fullPath).replaceAll(path.sep, "/");
  const parts = relative.split("/").filter(Boolean);
  const fileName = parts.pop() || "documentation.md";
  const stem = fileName.replace(/\.md$/i, "");
  const segments = /^readme$/i.test(stem) ? [...parts.map(slugify), "overview"] : [...parts.map(slugify), slugify(stem)];
  return `/docs/${segments.join("/")}`;
}

const markdownFiles = walk(docsRoot);
if (!markdownFiles.length) failures.push("No Markdown documentation was discovered.");

const routes = new Map();
for (const file of markdownFiles) {
  if (path.relative(docsRoot, file).replaceAll(path.sep, "/") === "README.md") continue;
  const route = routeForSource(file);
  if (routes.has(route)) failures.push(`Route collision: ${route} is generated by both ${routes.get(route)} and ${path.relative(root, file)}.`);
  routes.set(route, path.relative(root, file));
}

const assetRegisterPath = path.join(docsRoot, "client-operations/legal/contracts/ASSET-REGISTER.md");
const assetRegister = existsSync(assetRegisterPath) ? readFileSync(assetRegisterPath, "utf8") : "";
if (!assetRegister) failures.push("The contract asset register is required for verified download governance.");

let checkedLinks = 0;
for (const file of markdownFiles) {
  const source = readFileSync(file, "utf8");
  const sourceDirectory = path.dirname(file);
  const links = Array.from(source.matchAll(/!?\[[^\]]*\]\(([^)\s]+)(?:\s+[^)]*)?\)/g), (match) => match[1]);

  for (const rawTarget of links) {
    const target = rawTarget.replace(/^<|>$/g, "").split("#")[0];
    if (!target || /^(https?:|mailto:|tel:|data:)/i.test(target)) continue;
    checkedLinks += 1;

    if (target.startsWith("/docs/")) {
      if (!routes.has(target.replace(/\/$/, ""))) failures.push(`${path.relative(root, file)} links to missing portal route ${target}.`);
      continue;
    }

    const resolved = path.resolve(sourceDirectory, decodeURIComponent(target));
    if (!resolved.startsWith(docsRoot)) {
      failures.push(`${path.relative(root, file)} links outside /docs: ${target}.`);
      continue;
    }

    if (!existsSync(resolved)) {
      failures.push(`${path.relative(root, file)} links to missing source ${target}.`);
      continue;
    }

    if (target.toLowerCase().endsWith(".pdf") && resolved.includes(`${path.sep}legal${path.sep}contracts${path.sep}`)) {
      const fileName = path.basename(resolved);
      if (!assetRegister.includes(fileName)) failures.push(`Contract PDF ${fileName} is linked but not recorded in ASSET-REGISTER.md.`);
    }

    if (target.endsWith("/") && !statSync(resolved).isDirectory()) failures.push(`${path.relative(root, file)} expects ${target} to be a directory.`);
  }
}

const packageJson = JSON.parse(readFileSync(path.join(root, "package.json"), "utf8"));
for (const dependency of ["react-markdown", "remark-gfm", "rehype-slug", "rehype-autolink-headings", "rehype-highlight"]) {
  if (!packageJson.dependencies?.[dependency]) failures.push(`${dependency} must be declared in package.json.`);
}
if (!packageJson.scripts?.["validate:docs"]) failures.push("package.json must define validate:docs.");

if (failures.length) {
  console.error("Documentation portal validation failed:\n");
  for (const failure of failures) console.error(`- ${failure}`);
  process.exit(1);
}

console.log(`Documentation portal validation passed: ${markdownFiles.length} Markdown sources, ${routes.size} generated document routes, ${checkedLinks} internal links checked.`);
